# Favorite Fruit: Make a list of your favorite fruits,
# and then write a series of independent if statements
# that check for certain fruits in your list.

#   - Make a list of your three favorite fruits and call it 
#     favorite_fruits

favorite_fruits = ['bananas', 'mango', 'pineapple']

#   - Write five if statements. Each should check whether
#     a certain kind of fruit is in your list, the if
#     block should print a statement, such as "You really
#     like bananas!"

if 'bananas' in favorite_fruits:
    print("You really like bananas!")
if 'strawberries' in favorite_fruits:
    print("You really like strawberries!")
if 'mango' in favorite_fruits:
    print("You really like mango!")
if 'blueberries' in favorite_fruits:
    print("You really like blueberries!")
if 'pineapple' in favorite_fruits:
    print("You really like pineapple!")



